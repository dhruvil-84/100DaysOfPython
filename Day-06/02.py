'''
for vs while loop

for loop:
--> Entry Controlled Loop.
--> used when we know how many times we have to iterate.
--> use forloop when you want to iterate over an iterable.

while loop:
--> Entry Controlled Loop.
--> used when we dont know how many times we have to iterate but we know a condition till when we have to iterate.
--> use whileloop when you dont care what number in a sequence you are in, which item you are iterating through in a list and you just simply want to carry out some sort of functionality many, many times until some sort of condition you set.

Syntax:
while some_condition_is_true:
	# Do this...
	# Then do this...
	# Finally do this...
'''